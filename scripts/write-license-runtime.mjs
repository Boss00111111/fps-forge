/**
 * Writes electron/license-runtime-url.cjs from FPSFORGE_LICENSE_BUNDLE_API.
 * Run automatically before `npm run dist`. Empty env → empty string (localhost fallback for dev builds).
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const target = path.join(root, "electron", "license-runtime-url.cjs");
const url = String(process.env.FPSFORGE_LICENSE_BUNDLE_API || "")
  .trim()
  .replace(/\/$/, "");
const content = `/* auto-generated by scripts/write-license-runtime.mjs — do not commit */
module.exports = ${JSON.stringify(url)};
`;
fs.writeFileSync(target, content, "utf8");
// eslint-disable-next-line no-console
console.log(url ? `[license-url] bundled: ${url}` : "[license-url] empty (use localhost or license-api.json)");
